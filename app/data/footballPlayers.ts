export function getFootballPlayersByTeam(teamId: string): FootballPlayer[] {
  return footballPlayers.filter(player => player.teamId === teamId);
}

export function getFootballPlayerById(id: string): FootballPlayer | undefined {
  return footballPlayers.find(player => player.id === id);
}

export function searchFootballPlayers(query: string): FootballPlayer[] {
  const lowerQuery = query.toLowerCase();
  return footballPlayers.filter(
    player =>
      player.name.toLowerCase().includes(lowerQuery) ||
      player.team.toLowerCase().includes(lowerQuery) ||
      player.position.toLowerCase().includes(lowerQuery) ||
      player.nationality.toLowerCase().includes(lowerQuery)
  );
}

export function getFootballPlayersByPosition(position: FootballPlayer['position']): FootballPlayer[] {
  return footballPlayers.filter(player => player.position === position);
}
