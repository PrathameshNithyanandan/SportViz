export function getFootballTeamById(id: string): FootballTeam | undefined {
  return footballTeams.find(team => team.id === id);
}

export function getFootballTeamByName(name: string): FootballTeam | undefined {
  return footballTeams.find(team => team.name.toLowerCase() === name.toLowerCase());
}

export function getFootballTeamsByType(type: 'club' | 'national'): FootballTeam[] {
  return footballTeams.filter(team => team.type === type);
}

export function getFootballTeamsByLeague(league: string): FootballTeam[] {
  return footballTeams.filter(team => team.league === league);
}
